import 'package:equatable/equatable.dart';
import 'package:flip_card/flip_card.dart';
import 'package:levels222_0/pages/home.dart';

enum LetterStatus { initial, notInWord, inWord, correct }

class Board extends StatelessWidget {
  const Board({
    super.key,
    required this.board,
    required this.flipCardKeys,
  });
  final List<List<GlobalKey<FlipCardState>>> flipCardKeys;
  final List<Word> board;

  @override
  Widget build(BuildContext context) {
    return Column(
      children: board
          .asMap()
          .map(
            (i, word) => MapEntry(
              i,
              Row(
                mainAxisAlignment: MainAxisAlignment.center,
                children: word.letters
                    .asMap()
                    .map((j, letter) => MapEntry(
                        j,
                        FlipCard(
                            key: flipCardKeys[i][j],
                            flipOnTouch: false,
                            direction: FlipDirection.VERTICAL,
                            front: BoardTile(
                              letter: Letter(
                                  val: letter.val,
                                  status: LetterStatus.initial),
                            ),
                            back: BoardTile(letter: letter))))
                    .values
                    .toList(),
              ),
            ),
          )
          .values
          .toList(),
    );
  }
}

// EACH TILE
class BoardTile extends StatelessWidget {
  const BoardTile({
    super.key,
    required this.letter,
  });

  final Letter letter;

  @override
  Widget build(BuildContext context) {
    return Container(
      margin: EdgeInsets.all(deviceWidth(context) / 150),
      height: deviceWidth(context) / 7,
      width: deviceWidth(context) / 7,
      alignment: Alignment.center,
      decoration: BoxDecoration(
        color: letter.backgroundColor,
        border: Border.all(color: letter.borderColor),
        borderRadius: BorderRadius.circular(deviceWidth(context) / 75),
      ),
      child: appText(letter.val, AppColors.lightGrey, deviceWidth(context) / 12,
          FontWeight.w700),
    );
  }
}

class Letter extends Equatable {
  const Letter({
    required this.val,
    this.status = LetterStatus.initial,
  });
  factory Letter.empty() => const Letter(val: '');
  final String val;
  final LetterStatus status;

  Color get backgroundColor {
    switch (status) {
      case LetterStatus.initial:
        return Colors.transparent;
      case LetterStatus.notInWord:
        return AppColors.darkerGrey;
      case LetterStatus.inWord:
        return const Color.fromARGB(255, 224, 252, 14);
      case LetterStatus.correct:
        return const Color.fromARGB(255, 18, 215, 48);
    }
  }

  Color get borderColor {
    switch (status) {
      case LetterStatus.initial:
        return AppColors.lightGrey;
      default:
        return Colors.transparent;
    }
  }

  Letter copyWith({
    String? val,
    LetterStatus? status,
  }) {
    return Letter(val: val ?? this.val, status: status ?? this.status);
  }

  @override
  List<Object?> get props => [val, status];
}

// WORD
class Word extends Equatable {
  const Word({required this.letters});

  factory Word.fromString(String word) =>
      Word(letters: word.split('').map((e) => Letter(val: e)).toList());

  final List<Letter> letters;

  String get wordString => letters.map((e) => e.val).join();

  void addLetter(String val) {
    final currentIndex = letters.indexWhere((e) => e.val.isEmpty);
    if (currentIndex != -1) {
      letters[currentIndex] = Letter(val: val);
    }
  }

  void removeLetter() {
    final recentLtterIndex = letters.lastIndexWhere((e) => e.val.isNotEmpty);
    if (recentLtterIndex != -1) {
      letters[recentLtterIndex] = const Letter(val: '');
    }
  }

  @override
  List<Object?> get props => [letters];
}

